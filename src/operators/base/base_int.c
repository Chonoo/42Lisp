int		ft_add_int(int a, int b)
{
	return (a + b);
}

int		ft_sub_int(int a, int b)
{
	return (a - b);
}

int		ft_mul_int(int a, int b)
{
	return (a * b);
}

int		ft_div_int(int a, int b)
{
	return (a / b);
}

int		ft_mod_int(int a, int b)
{
	return (a % b);
}
